package com.training;

public class CurrencyConverter {
	
	public double inrToUsd(double inr) {
		
		return inr*0.013;
	}
	
	public double inrToEuro(Double inr) {
		return inr*0.012;
	}
	
	public Integer value(Integer a) {
		return a+100;
	}

	public int value(int a) {
		return a;
	}
	}
